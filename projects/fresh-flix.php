<h3>Reflections</h3>
    <p>As I mentioned earlier, our new Github skills were put to the test in this project. We began overly cautious, making a local copy of our work every time we went to merge for fear of losing anything. However, as time went on and we figured out our rythm as a team with Github, it became easier and easier to deal with the occasional merge conflict or accidental merge to the master.</p>
    <p>This was the first major project that I worked with a team on, and although there was some added complication with sharing our work via Github, once we figured that out, the ability to collaborate, share ideas, and see my design grow with their input was very rewarding in the end. I'm very proud of this site. Please enjoy!</p>



