        <p class="project-summary"> Fresh Flix is a dynamic movie database I made with a group of two others. Using <a href="https://www.themoviedb.org/"> The Movie DB's </a> API connection, this React Application dynamically updates to show the most up to date data dependant on the user's filtering options. A user can filter movies by 'popular', 'top rated', 'upcoming', or movies 'now playing'. A user can also favourite movies that interest them, those movies are stored on their local storage and displayed to them on the 'favourites' page.  </p>
