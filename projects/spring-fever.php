<h3>Reflections</h3>
    <p>When we were first given this project it felt almost impossible that we'd be able to begin creating a game within our first month; our projects before this point had been HTML and CSS with minor JavaScript functionality. However, once I started building the game and functionality, I discovered that the problem solving and the feeling I got when I solved that problem, were a lot of fun and I even found myself waking up in the middle of the night to try a solution to a bug that I had thought of in a dream!</p>
    <p>This project is still far from perfect, there is a bug that haunts me to this day that I work on every now and then, but I am still very proud of it and like to look at it to see where I started and how far I've come since. </p>
